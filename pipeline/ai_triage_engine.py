def compute_sla_deadline(severity: str, now: datetime) -> str:
    hours = SLA_HOURS.get(severity, 168)
    deadline = now + timedelta(hours=hours)
    return deadline.strftime("%Y-%m-%dT%H:%M:%SZ")

