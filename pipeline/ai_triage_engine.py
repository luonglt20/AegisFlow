    confidence_map = {"CRITICAL": 90, "HIGH": 80, "MEDIUM": 70, "LOW": 60}
    return {
        "classification": "True Positive",
        "confidence":     confidence_map.get(sev, 70),
        "reasoning":      (
            f"Static analysis indicates this {sev.lower()}-severity finding "
            f"({title[:60]}) is likely exploitable based on code context. "
            "Manual verification is recommended to confirm the code path is reachable "
            "and no compensating control exists upstream in the request pipeline."
        ),
        "code_fix": {
            "before":      finding.get("code_snippet", "See finding details."),
            "after":       "Refer to remediation_hint field for fix guidance.",
            "explanation": finding.get("remediation_hint", "Apply security best practices for this vulnerability class."),
        },
        "business_impact": finding.get("business_impact", "Security risk — see finding details."),
        "priority_score":  {"CRITICAL": 9, "HIGH": 7, "MEDIUM": 5, "LOW": 2}.get(sev, 5),
    }

