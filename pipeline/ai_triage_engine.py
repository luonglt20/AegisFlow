# Classification color
# ─────────────────────────────────────────────────────────────

def classification_color(cls: str) -> str:
    mapping = {
        "True Positive":  (RED,    "🔴 True Positive"),
        "False Positive": (GREEN,  "✅ False Positive"),
        "Needs Review":   (YELLOW, "🔶 Needs Review"),
    }
    color, label = mapping.get(cls, (DIM, cls))
    return c(label, color)

def triage_finding(finding: dict, target_path: str, mode: str):
    """Worker function for parallel triage"""
    now = datetime.now(timezone.utc)
    severity = finding.get("severity", "MEDIUM")

    # ── Get AI Context ──────────────────────────────────
    file_path = finding.get("affected_file", "")
    line_num = finding.get("affected_line", 0)
    context = get_code_context(target_path, file_path, line_num)
    finding["code_context"] = context

    # ── Get AI response ───────────────────────────────────
    ai_resp = None
    if mode == "LIVE":
        ai_resp = call_groq(finding, context)
        if not ai_resp:
             # Fallback
             mock_key = resolve_mock_key(finding)
             ai_resp = default_mock(finding)
    else:
        mock_key = resolve_mock_key(finding)
        ai_resp = default_mock(finding)

    # ── Apply results ───────────────────────────────────
    classification = ai_resp.get("classification", "Needs Review")
    finding["ai_analysis"] = f"[{classification}] {ai_resp.get('reasoning', '')}"
    finding["ai_fix"] = ai_resp.get("code_fix", {})
    finding["ai_confidence"] = ai_resp.get("confidence", 70)
    finding["ai_priority_score"] = ai_resp.get("priority_score", 5)
    finding["business_impact"] = ai_resp.get("business_impact", "")
    finding["status"] = "PENDING_HUMAN_VERIFY"
    finding["ai_model"] = GROQ_MODEL if mode == "LIVE" else f"mock:{GROQ_MODEL}"
    finding["ai_analyzed_at"] = now.strftime("%Y-%m-%dT%H:%M:%SZ")

    # Simple console output
    print(f"  [TRIAGED] {finding.get('id')} -> {classification}")

