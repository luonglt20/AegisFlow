def call_groq(finding: dict, context: str = "") -> Optional[dict]:
    """
    Call Groq API and return parsed JSON response dict.
    Returns None on any error.
    """
    payload = json.dumps({
        "model": GROQ_MODEL,
        "messages": [
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user",   "content": build_user_prompt(finding, context)},
        ],
        "temperature":  0.3,
        "max_tokens":   2048,
        "response_format": {"type": "json_object"},
    }).encode("utf-8")

    req = urllib.request.Request(
        GROQ_URL,
        data=payload,
        headers={
            "Content-Type":  "application/json",
            "Authorization": f"Bearer {GROQ_API_KEY}",
            "User-Agent":    "Mozilla/5.0 (AegisFlow DevSecOps AI Copilot)",
        },
        method="POST",
    )

    try:
        with urllib.request.urlopen(req, timeout=GROQ_TIMEOUT) as resp:
            body = resp.read().decode("utf-8")
    except urllib.error.HTTPError as exc:
        err_body = exc.read().decode("utf-8", errors="replace")[:300]
        print(c(f"\n    [API ERROR] HTTP {exc.code}: {err_body}", RED), file=sys.stderr)
        return None
    except urllib.error.URLError as exc:
        print(c(f"\n    [API ERROR] Connection failed: {exc.reason}", RED), file=sys.stderr)
        return None
    except Exception as exc:
        print(c(f"\n    [API ERROR] Unexpected: {exc}", RED), file=sys.stderr)
        return None

    try:
        groq_resp = json.loads(body)
        content   = groq_resp["choices"][0]["message"]["content"]
        # Strip potential markdown fences defensively
        content   = content.strip()
        if content.startswith("```"):
            content = content.split("```")[1]
            if content.startswith("json"):
                content = content[4:]
        return json.loads(content)
    except (KeyError, IndexError, json.JSONDecodeError) as exc:
        print(c(f"\n    [PARSE ERROR] Could not parse Groq response: {exc}", RED), file=sys.stderr)
        return None

